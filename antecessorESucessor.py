'''
Construir um programa que leia um valor numérico inteiro N e apresentar como resultado os seus
valores sucessor e antecessor sem o uso de atribuição a uma variável de saída.

'''

# Lendo o valor de N
N = int(input("Digite um valor numérico inteiro: "))

# Apresentando o sucessor e o antecessor
print("O sucessor de", N, "é", N + 1)
print("O antecessor de", N, "é", N - 1)
