'''
Faça um programa que receba quatro números inteiros, calcule e mostre a soma desses números.

'''

# Solicitando os quatro números inteiros ao usuário
numero1 = int(input("Digite o primeiro número inteiro: "))
numero2 = int(input("Digite o segundo número inteiro: "))
numero3 = int(input("Digite o terceiro número inteiro: "))
numero4 = int(input("Digite o quarto número inteiro: "))

# Calculando a soma dos números
soma = numero1 + numero2 + numero3 + numero4

# Exibindo o resultado da soma
print("A soma dos quatro números é:", soma)
